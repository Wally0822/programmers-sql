-- ✅ MySQL 버전
SELECT 
    D.DR_NAME, 
    D.DR_ID, 
    D.MCDP_CD, 
    DATE_FORMAT(D.HIRE_YMD, '%Y-%m-%d') AS HIRE_YMD
FROM DOCTOR D
WHERE D.MCDP_CD = 'CS' OR D.MCDP_CD = 'GS'
ORDER BY D.HIRE_YMD DESC, D.DR_NAME ASC;

-- ✅ Oracle 버전
SELECT 
    D.DR_NAME,
    D.DR_ID,
    D.MCDP_CD,
    TO_CHAR(D.HIRE_YMD, 'YYYY-MM-DD') AS HIRE_YMD
FROM DOCTOR D
WHERE D.MCDP_CD = 'CS' OR D.MCDP_CD = 'GS'
ORDER BY D.HIRE_YMD DESC, D.DR_NAME ASC;
